public class Relational {
    public static void main(String[] args) {
        int num1 = 10, num2 = 15;

        System.out.println(num1 == num2); //false
        System.out.println(num1 != num2); //true
        System.out.println(num1 < num2); //true
        System.out.println(num1 > num2); //false
        System.out.println(num1 <= num2); //true
        System.out.println(num1 >= num2); //false
    }
}
